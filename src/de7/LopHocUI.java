    private void btnCreateLopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateLopActionPerformed
        String tenLop = txTenlop.getText().trim();
        Integer siSo = Integer.parseInt(txSs.getText().trim());

        if (tenLop.isEmpty() | siSo < 0) {
            JOptionPane.showMessageDialog(this, "Dữ liệu nhập vào không hợp lệ !");
            return;
        }

        if (lopHoc == null) {
            this.lopHoc = new LopHoc(siSo, tenLop);
        } else {
            JOptionPane.showMessageDialog(null, "Bạn đã khởi tạo lớp học rồi mà !!!");
        }
    }//GEN-LAST:event_btnCreateLopActionPerformed

    private void btnAddStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStudentActionPerformed
        if (lopHoc == null) {
            JOptionPane.showMessageDialog(null, "Bạn chưa có lớp học. Hãy tạo trước khi thêm sinh viên.");
            return;
        }

        String hoTen = txHoten.getText().trim();
        String msv = txMsv.getText().trim();
        try {
            Float diemqt = Float.parseFloat(txdqt.getText().trim());
            Float diemhp = Float.parseFloat(txdhp.getText().trim());

            if (hoTen.isEmpty() || msv.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Dữ liệu sinh viên không hợp lệ.");
                return;
            }

            if (lopHoc.themSinhVien(new SinhVien(hoTen, msv, diemqt, diemhp))) {
                JOptionPane.showMessageDialog(null, "Thêm sinh viên thứ " + countSV++ + "/" + lopHoc.getSiSo() + " vào lớp thành công !!!");
                loadDataToTable();
            } else {
                JOptionPane.showMessageDialog(this, "Lớp đã đầy sinh viên");
            }
        } catch(NumberFormatException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddStudentActionPerformed

