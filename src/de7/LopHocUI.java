    private void loadDataToTable() {
        ArrayList<SinhVien> dsSV = lopHoc.getDsSinhVien();
        DefaultTableModel dtm = (DefaultTableModel) tableSV.getModel();

        dtm.setRowCount(0);
        for (SinhVien sv : dsSV) {
            dtm.addRow(new Object[]{
                sv.getMaSinhVien(),
                sv.getHoTen(),
                sv.getDiemQT(),
                sv.getDiemHP(),
                sv.diemTB()
            });
        }

        tableSV.setModel(dtm);
    }

